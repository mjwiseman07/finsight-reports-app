/**
 * One-shot generator: parses docs/govcon/wave1/*.md citation tables into handles-registry.ts
 */
const fs = require("fs");
const path = require("path");

const root = path.resolve(__dirname, "..");
const sourceDir = path.join(root, "docs/govcon/wave1");
const outPath = path.join(
  root,
  "lib/intelligence/synthetic/standards/govcon/handles-registry.generated.ts",
);

const SOURCE_FILES = [
  { file: "GovCon_FAR31_Sources.md", library: "FAR_PART_31" },
  { file: "GovCon_CAS_Sources.md", library: "CAS" },
  { file: "GovCon_DCAA_Sources.md", library: "DCAA" },
  { file: "GovCon_Disclosures_Sources.md", library: "DISCLOSURES" },
  { file: "GovCon_Benchmarks_Sources.md", library: "BENCHMARKS" },
];

const DFARS_HANDLES = [
  {
    handleId: "DFARS_252_242_7006",
    library: "DCAA",
    title: "DFARS 252.242-7006 — Accounting System",
    url: "https://www.acquisition.gov/dfars/252.242-7006-accounting-system.",
  },
  {
    handleId: "DFARS_252_242_7005",
    library: "DCAA",
    title: "DFARS 252.242-7005 — Contractor Purchasing System",
    url: "https://www.acquisition.gov/dfars/252.242-7005-contractor-purchasing-system.",
  },
  {
    handleId: "DFARS_252_215_7002",
    library: "DCAA",
    title: "DFARS 252.215-7002 — Cost Estimating System",
    url: "https://www.acquisition.gov/dfars/252.215-7002-cost-estimating-system.",
  },
  {
    handleId: "DFARS_215_407_3",
    library: "DCAA",
    title: "DFARS 215.407-3 — Forward Pricing Rate Agreements",
    url: "https://www.acquisition.gov/dfars/215.407-3-forward-pricing-rate-agreements.",
  },
];

const ROW_RE =
  /\|\s*`([^`]+)`\s*(?:\|[^|\r\n]*)+\|\s*\[[^\]]*\]\((https?:\/\/[^)]+)\)/g;

function parseMarkdown(filePath, library) {
  const content = fs.readFileSync(filePath, "utf8");
  const entries = [];
  let match;
  while ((match = ROW_RE.exec(content)) !== null) {
    entries.push({
      handleId: match[1].trim(),
      library,
      url: match[2].trim(),
    });
  }
  return entries;
}

function main() {
  const byId = new Map();

  for (const { file, library } of SOURCE_FILES) {
    const absolute = path.join(sourceDir, file);
    if (!fs.existsSync(absolute)) {
      throw new Error(`Missing source doc: ${absolute}`);
    }
    for (const entry of parseMarkdown(absolute, library)) {
      if (!byId.has(entry.handleId)) {
        byId.set(entry.handleId, entry);
      }
    }
  }

  for (const entry of DFARS_HANDLES) {
    byId.set(entry.handleId, entry);
  }

  const sorted = [...byId.values()].sort((a, b) => a.handleId.localeCompare(b.handleId));

  const body = sorted
    .map(
      (e) =>
        `  ${JSON.stringify(e.handleId)}: { handleId: ${JSON.stringify(e.handleId)}, library: ${JSON.stringify(e.library)}, url: ${JSON.stringify(e.url)} },`,
    )
    .join("\n");

  const fileContent = `/**
 * AUTO-GENERATED by scripts/gen-govcon-handles-registry.js — do not edit manually.
 * containsGovernmentContractData: true
 * Source: docs/govcon/wave1/*.md + 4 DFARS handles (Q11=A).
 * Generated: ${new Date().toISOString()}
 * Handle count: ${sorted.length}
 */

import type { GovConCitationHandle, GovConCitationLibrary } from "./__init__/types";

export const GOVCON_CITATION_HANDLE_REGISTRY: Record<string, GovConCitationHandle> = {
${body}
};

export const GOVCON_CITATION_HANDLE_COUNT = ${sorted.length};

export const GOVCON_CITATION_LIBRARIES = ${JSON.stringify(
    [...new Set(sorted.map((e) => e.library))].sort(),
  )} as const satisfies readonly GovConCitationLibrary[];
`;

  fs.mkdirSync(path.dirname(outPath), { recursive: true });
  fs.writeFileSync(outPath, fileContent, "utf8");
  console.log(`Wrote ${sorted.length} handles to ${path.relative(root, outPath)}`);
}

main();
